const star = {
    3: [
        '  *  ',
        ' * * ',
        '*****'
    ]
};
let minStar = 3;

function solution(scanner) {
    // 3, 6, 12, 24, 48, ... (3 * 2^N)
    let N = scanner.readInt();
    while (!star[N]) {
        makeNextStar(minStar * 2);
        minStar *= 2;
    }
    return star[N].join('\n');
}

function makeNextStar(N) {
    const parentN = Math.floor(N / 2);
    const newArr = [...star[String(parentN)], ...star[String(parentN)]];
    let margin = '';
    for (let i = 0; i < parentN; i++) {
        margin += ' ';
    }
    for (let i = 0; i < parentN; i++) {
        newArr[i] = margin + newArr[i] + margin;
    }
    for (let i = parentN; i < N; i++) {
        newArr[i] += ' ' + newArr[i];
    }
    star[N] = newArr;
}

