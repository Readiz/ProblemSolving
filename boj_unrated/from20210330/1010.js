function solution(scanner) {
    const T = scanner.readInt();
    const r = [];
    for (let tc = 0; tc < T; tc++) {
        const N = scanner.readInt();
        const M = scanner.readInt();
        r.push(combination(M, N));
    }
    return r.join('\n');
}

function combination(N, M) {
    let r = 1;
    for (let i = 1; i <= M; i++) {
        r *= N;
        N--;
        r = Math.floor(r / i);
    }
    return r;
}

