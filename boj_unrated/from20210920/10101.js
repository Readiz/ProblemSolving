function solution(scanner) {
    const A = scanner.readInt();
    const B = scanner.readInt();
    const C = scanner.readInt();
    
    if (A + B + C !== 180) {
        return 'Error';
    }
    if (A === B && B === C) {
        return 'Equilateral';
    }
    if (A === B || B === C || A === C) {
        return 'Isosceles';
    }
    return 'Scalene';
}

