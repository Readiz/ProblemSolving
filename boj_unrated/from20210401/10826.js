function solution(scanner) {
    const N = scanner.readInt();
    if (N === 0) return 0;
    if (N === 1) return 1;
    let pprev = 0n, prev = 1n, cur = 1n;
    curIdx = 2;
    while (curIdx < N) {
        pprev = prev;
        prev = cur;
        cur = pprev + prev;
        curIdx++;
    }
    return cur;
}

