function solution(scanner) {
    const [N, K] = scanner.readLine().split(' ').map(Number);
    const db = [];
    const r = [];
    for (let i = 1; i <= N; i++) {
        db.push(i);
    }
    let p = K - 1; // 시작지점 세팅
    while (db.length > 0) {
        r.push(db.splice(p, 1));
        p += K - 1; // 하나 빠진 상태에서 다음꺼 찾으니까..
        p = p % db.length;
    }
    return '<' + r.join(', ') + '>';
}


