function solution(scanner) {
    let N = scanner.readInt();
    const countDB = new Map();
    for (let i = 0; i < N; i++) {
        const val = scanner.readInt();
        if (countDB.has(val)) countDB.set(val, countDB.get(val) + 1);
        else countDB.set(val, 1);
    }
    const targetNum = scanner.readInt();

    return (countDB.has(targetNum) ? countDB.get(targetNum) : 0)
}


