function solution(scanner) {
    const T = scanner.readInt();
    const r = [];
    for (let tc = 0; tc < T; tc++) {
        let db = [];
        for (let i = 0; i < 10; i++) {
            db.push(scanner.readInt());
        }
        db.sort((a, b) => a - b);
        r.push(db[7]);
    }
    return r.join('\n');
}


