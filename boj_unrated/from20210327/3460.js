function solution(scanner) {
    const T = scanner.readInt();
    const r = [];
    for (let i = 0; i < T; i++) {
        let N = scanner.readInt();
        let p = 0;
        while (N > 0) {
            console.log(N);
            const R = N % 2;
            if (R === 1) r.push(p);
            N = Math.floor(N / 2);
            p++;
        }
    }
    return r.join(' ');
}


