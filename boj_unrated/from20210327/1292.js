function solution(scanner) {
    const A = scanner.readInt();
    const B = scanner.readInt();
    let N = 1;
    const db = [1];
    while (db.length <= B) {
        const newN = N + 1;
        for (let i = 0; i < newN; i++) {
            db.push(newN);
        }
        N = newN;
    }
    const cut = db.slice(A - 1, B);
    console.log(cut);
    return cut.reduce((_, a) => _ + a);
}


