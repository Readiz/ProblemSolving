function solution(scanner) {
    let r = '';
    const line = scanner.readLine();
    const db = ['C', 'A', 'M', 'B', 'R', 'I', 'D', 'G', 'E'];
    for (let i = 0; i < line.length; i++) {
        if (db.indexOf(line[i]) > -1) {
            // do nothing...
        } else {
            r += line[i];
        }
    }
    return r;
}

