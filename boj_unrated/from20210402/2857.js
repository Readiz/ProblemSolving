function solution(scanner) {
    const r = [];
    let N = 5;
    for (let i = 1; i <= N; i++) {
        const line = scanner.readLine();
        if (line.indexOf('FBI') > -1) {
            r.push(i);
        }
    }
    if (r.length > 0) {
        return r.join(' ');
    }
    return 'HE GOT AWAY!';
}

