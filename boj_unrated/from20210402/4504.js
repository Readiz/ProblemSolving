function solution(scanner) {
    const N = scanner.readInt();
    const r = [];
    while (true) {
        const t = scanner.readInt();
        if (t === 0) break;
        if (t % N === 0) {
            r.push(`${t} is a multiple of ${N}.`);
        } else {
            r.push(`${t} is NOT a multiple of ${N}.`);
        }
    }
    return r.join('\n');
}

