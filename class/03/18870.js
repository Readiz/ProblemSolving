function solution(scanner) {
    const N = scanner.readInt();
    const db = [];
    for (let i = 0; i < N; i++) {
        db.push(scanner.readInt());
    }
    const indexer = [...new Set(db)].sort((a, b) => a - b);
    const hashedIndexer = new Map();
    for (let i = 0; i < indexer.length; i++) {
        hashedIndexer.set(indexer[i], i);
    }
    const r = [];
    for (let i = 0; i < N; i++) {
        r.push(hashedIndexer.get(db[i]));
    }
    return r.join(' ');
}


